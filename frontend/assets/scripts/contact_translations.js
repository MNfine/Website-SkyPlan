// Export for usage in contact.js
if (typeof module !== 'undefined' && module.exports) {
    module.exports = contactTranslations;
}

// Function to apply contact-specific translations
function applyContactTranslations(lang) {
    console.log('Applying contact translations for language:', lang);
    
    const elements = document.querySelectorAll('[data-i18n]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (contactTranslations[lang] && contactTranslations[lang][key]) {
            if (key === 'addressContent' || key === 'hoursContent') {
                element.innerHTML = contactTranslations[lang][key];
            } else {
                element.textContent = contactTranslations[lang][key];
            }
        }
    });
    
    // Update placeholders
    const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
    placeholderElements.forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (contactTranslations[lang] && contactTranslations[lang][key]) {
            element.placeholder = contactTranslations[lang][key];
        }
    });
    
    // Update page title and meta description
    if (contactTranslations[lang]['metaTitle']) {
        document.title = contactTranslations[lang]['metaTitle'];
    }
    
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && contactTranslations[lang]['metaDescription']) {
        metaDesc.setAttribute('content', contactTranslations[lang]['metaDescription']);
    }
    
    console.log('Contact translations applied successfully for:', lang);
}

// Function to change language
function changeLanguage(lang) {
    localStorage.setItem('preferredLanguage', lang);
    document.documentElement.lang = lang;
    applyContactTranslations(lang);
    
    console.log('Contact page language changed to:', lang);
}

// Ensure Vietnamese is set as default on first visit
if (!localStorage.getItem('preferredLanguage')) {
    localStorage.setItem('preferredLanguage', 'vi');
}