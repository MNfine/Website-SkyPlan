(function () {
  function getCheckinLanguage() {
    if (typeof window.getPersistedLanguage === "function") {
      return window.getPersistedLanguage() === "en" ? "en" : "vi";
    }

    const languageRaw = (localStorage.getItem("language") || "").toLowerCase();
    if (languageRaw === "en" || languageRaw === "vi") return languageRaw;

    const preferredRaw = (localStorage.getItem("preferredLanguage") || "").toLowerCase();
    if (preferredRaw === "en" || preferredRaw === "vi") return preferredRaw;

    return (document.documentElement.lang || "vi").toLowerCase() === "en" ? "en" : "vi";
  }

  function getCheckinDict(lang) {
    return checkinOnlineTranslations[lang] || checkinOnlineTranslations.vi;
  }

  function t(key, lang) {
    const dict = getCheckinDict(lang || getCheckinLanguage());
    return dict[key] || key;
  }

  function applyCheckinTranslations(lang) {
    const activeLang = lang || getCheckinLanguage();
    const dict = getCheckinDict(activeLang);

    document.documentElement.lang = activeLang;
    document.title = dict.pageTitle;

    const metaDescription = document.querySelector('meta[name="description"]');
    if (metaDescription && dict.metaDescription) {
      metaDescription.setAttribute("content", dict.metaDescription);
    }

    document.querySelectorAll("[data-i18n]").forEach((element) => {
      const key = element.getAttribute("data-i18n");
      if (!key || !(key in dict)) return;
      element.textContent = dict[key];
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
      const key = element.getAttribute("data-i18n-placeholder");
      if (!key || !(key in dict)) return;
      element.setAttribute("placeholder", dict[key]);
    });

    document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
      const key = element.getAttribute("data-i18n-aria-label");
      if (!key || !(key in dict)) return;
      element.setAttribute("aria-label", dict[key]);
    });
  }

  window.CheckinOnlineI18n = {
    translations: checkinOnlineTranslations,
    getLanguage: getCheckinLanguage,
    getDict: getCheckinDict,
    t,
    applyTranslations: applyCheckinTranslations,
  };

  document.addEventListener("languageChanged", (event) => {
    const nextLang = event?.detail?.lang || getCheckinLanguage();
    applyCheckinTranslations(nextLang);
  });
})();
